#案例:银行卡ATM取款
#需求:小智的银行卡中有10000元,现在到ATM进行取钱操作,请根据输入的金额执行取钱操作,取钱完毕后,展示其银行卡余额。
# 步骤:
# 1. 输入密码
# 2. 输入取款金额
# 3. 计算余额并输出

total = 10000 #总金额
#输入密码
password = input("请输入你的银行卡密码:")
print(f"密码正确,{password}")
#输入取款金额
num = input("请输入你的取款金额:")
#计算金额并输出
print(f"取款后的银行可余额为:{total-int(num)}")